create policy "Public read menus" on public.menus for select using (true);

drop policy if exists "Public read menu seasonal ingredients" on public.menu_seasonal_ingredients;
create policy "Public read menu seasonal ingredients" on public.menu_seasonal_ingredients for select using (true);

drop policy if exists "Public read menu ingredients" on public.menu_ingredients;
create policy "Public read menu ingredients" on public.menu_ingredients for select using (true);

drop policy if exists "Public read decor items" on public.decor_items;
create policy "Public read decor items" on public.decor_items for select using (true);

